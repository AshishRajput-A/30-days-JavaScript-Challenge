// Loop control statement

// Task-8 wap to print number 1 tp 10 skip 5 using continue statement

for (let i = 1; i <= 10; i++) {
  if (i === 5) {
    continue;
  }
  console.log(i);
}

//Task-9 wap to print number 1 tp 10 but stop at 7 number using break

for (let i = 1; i <= 10; i++) {
  if (i === 7) {
    break;
  }
  console.log(i);
}
